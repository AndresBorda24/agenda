<span class="text-aso-primary font-bold block mb-1 pl-2">Preguntas Frecuentes:</span>
<div id="faq" class="flex flex-col max-w-xl">
  <details>
    <summary>¿Qué es GOU Pagos?</summary>
    <p>
      GUO PAGOS es la plataforma de pagos electrónicos que usa Clínica Asotrauma para procesar en línea las transacciones generadas en la tienda virtual con las formas de pago habilitadas para tal fin.
    </p>
  </details>

  <details>
    <summary>¿Cómo puedo pagar?</summary>
    <p>
      En la tienda virtual de Clínica Asotrauma usted podrá realizar su pago con los medios habilitados para tal fin. Usted, de acuerdo a las opciones de pago escogidas por el comercio, podrá pagar a través de tarjetas de crédito Visa, y MasterCard y Cuentas debito ahorro y corriente PSE.
    </p>
  </details>

  <details>
    <summary>¿Es seguro ingresar mis datos bancarios en este sitio web?</summary>
    <p>
      Para proteger tus datos Clínica Asotrauma delega en GOU pagos la captura de la información sensible. Nuestra plataforma de pagos cumple con los más altos estándares exigidos por la norma internacional PCI DSS de seguridad en transacciones con tarjeta de crédito. Además tiene certificado de seguridad SSL expedido por GeoTrust una compañía Verisign, el cual garantiza comunicaciones seguras mediante la encriptación de todos los datos hacia y desde el sitio; de esta manera te podrás sentir seguro a la hora de ingresar la información de su tarjeta.
    </p>
    <p>
      Durante el proceso de pago, en el navegador se muestra el nombre de la organización autenticada, la autoridad que lo certifica y la barra de dirección cambia a color verde. Estas características son visibles de inmediato y dan garantía y confianza para completar la transacción en GOU Pagos.
    </p>
    <p>
      GOU Pagos también cuenta con el monitoreo constante de McAfee Secure y la firma de mensajes electrónicos con Certicámara.
    </p>
  </details>

  <details>
    <summary>¿Puedo realizar el pago cualquier día y a cualquier hora?</summary>
    <p>
      Sí, en Clínica Asotrauma podrás realizar tus compras en línea los 7 días de la semana, las 24 horas del día a sólo un clic de distancia.
    </p>
  </details>

  <details>
    <summary>¿Puedo cambiar la forma de pago?</summary>
    <p>
      Si aún no has finalizado tu pago, podrás volver al paso inicial y elegir la forma de pago que prefieras. Una vez finalizada la compra no es posible cambiar la forma de pago.
    </p>
    <p>
      ESTABLECIMIENTO DE COMERCIO: el punto anterior aplica a la forma de pago, pero deberán mencionar las políticas de devolución que tenga la tienda para dar cumplimiento al artículo 51 de la Ley del Estatuto del Consumidor.
    </p>
  </details>

  <details>
    <summary>¿Pagar electrónicamente tiene algún valor para mí como comprador?</summary>
    <p>
      No, los pagos electrónicos realizados a través de GOU Pagos no generan costos adicionales para el comprador.
    </p>
  </details>

  <details>
    <summary>¿Qué debo hacer si mi transacción no concluyó?</summary>
    <p>
      En primera instancia, revisar si llegó un email de confirmación de la transacción a la cuenta de correo electrónico inscrita en el momento de realizar el pago, en caso de no haberlo recibido, deberás contactar a <a href="mailto:tesoreria@asotrauma.com.co">Tesoreria Asotrauma</a> para confirmar el estado de la transacción.
    </p>
  </details>

  <details>
    <summary>¿Qué debo hacer si no recibí el comprobante de pago?</summary>
    <p>
      Por cada transacción aprobada a través de GOU Pagos, recibirás un comprobante del pago con la referencia de compra en la dirección de correo electrónico que indicaste al momento de pagar.
    </p>
    <p>
      Si no lo recibes, podrás contactar a <a class="underline font-bold md:no-underline md:hover:underline" href="mailto:tesoreria@asotrauma.com.co">Tesoreria Asotrauma</a> o a la línea <a class="underline font-bold md:no-underline md:hover:underline text-nowrap" href="tel:+57515300">(8) 515 - 3000</a> o al correo electrónico <a class="underline font-bold md:no-underline md:hover:underline" href="mailto:tesoreria@asotrauma.com.co">tesoreria@asotrauma.com.co</a>, para solicitar el reenvío del comprobante a la misma dirección de correo electrónico registrada al momento de pagar.
    </p>
  </details>
